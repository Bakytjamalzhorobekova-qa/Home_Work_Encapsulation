public class Cat {
    String name;
    String color;
    int age;
    int weight;

    public void setName(String name1){
        name=name1;
    }
    public String getName(){
        return name;
    }
    public void setColor(String color1){
        color=color1;
    }
    public String getColor(){
        return color;
    }
    public void setAge(int age1){
        age=age1;
    }
    public int getAge(){
        return age;
    }
    public void setWeight(int weight1){
        weight=weight1;
    }
    public int getWeight(){
        return weight;
    }

}
