public class Dog {
    String breed;
    int age;
    String color;
    public void setBreed(String breed1){
        breed=breed1;
    }
    public String getBreed(){
        return breed;
    }
    public void setAge(int age1){
        age=age1;
    }
    public int getAge(){
        return age;
    }
    public void setColor(String color1){
        color=color1;
    }
    public String getColor(){
        return color;
    }

}
